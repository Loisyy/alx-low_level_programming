#include <stdlib.h>
#include <stdio.h>


/**
 * add - add two integers
 * @a: first int
 * @b second int
 *return: addition 
 */
int add(int a, int b)
{
	return (a + b);
}	


/**
 * sub - subtract two integers
 * @a: first int
 * @b second int
 *return: subtracted 
 */
int sub(int a, int b)
{
	return (a - b);
}	


/**
 * mul - multiply two integers
 * @a: first int
 * @b second int
 *return: multiplication
 */
int mul(int a, int b)
{
	return (a * b);
}	


/**
 * mod - mod of two integers
 * @a: first int
 * @b second int
 *return: modulus 
 */
int mod(int a, int b)
{
	return (a % b);
}


/**
 * my_div - divide two integers
 * @a: first int
 * @b: second int
 * Return: division result 
 */
int my_div(int a, int b)
{
    return (a / b);
}
